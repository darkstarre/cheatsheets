==========================Booleans=======================
#description: true or false values

# && AND most both be true to evaluate true
if 5 > 3 && 7 > 3 #=> true

# || OR either can be true to evaluate true
if 5 > 3 || 5 > 7 #=> true

# if begins a statement that evaluates if true
if x(do these things)

# unless is a statement that evaluates if not true
unless x == true(do these things)

